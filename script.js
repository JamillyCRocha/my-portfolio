(() => {
  "use strict";

  (function(){
    const links = Array.from(document.querySelectorAll(".nav__link"));
    if (!links.length) return;

    const sections = links
      .map(a => document.querySelector(a.getAttribute("href")))
      .filter(Boolean);

    if (!sections.length) return;

    function setActiveById(id){
      links.forEach(l =>
        l.classList.toggle("is-active", l.getAttribute("href") === `#${id}`)
      );
    }

    const observer = new IntersectionObserver(entries => {
      const visible = entries
        .filter(e => e.isIntersecting)
        .sort((a,b) => b.intersectionRatio - a.intersectionRatio)[0];

      if (visible?.target?.id) setActiveById(visible.target.id);
    }, { threshold: [0.2, 0.35, 0.5, 0.65] });

    sections.forEach(sec => observer.observe(sec));
  })();

  (function(){
    const menuBtn = document.getElementById("menuBtn");
    const mobileNav = document.getElementById("mobileNav");
    if (!menuBtn || !mobileNav) return;

    function close(){
      mobileNav.hidden = true;
      menuBtn.setAttribute("aria-expanded", "false");
    }

    menuBtn.addEventListener("click", () => {
      const open = !mobileNav.hidden;
      mobileNav.hidden = open;
      menuBtn.setAttribute("aria-expanded", String(!open));
    });

    mobileNav.addEventListener("click", e => {
      if (e.target.closest("a")) close();
    });
  })();

  (function(){
    document.documentElement.classList.add("preload");
    const els = Array.from(document.querySelectorAll(".reveal"));

    if (els.length) {
      const obs = new IntersectionObserver(entries => {
        entries.forEach(e => {
          if (!e.isIntersecting) return;
          e.target.classList.add("is-in");
          obs.unobserve(e.target);
        });
      }, { threshold: 0.15, rootMargin: "0px 0px -10% 0px" });

      els.forEach(el => obs.observe(el));
    }

    window.addEventListener("load", () => {
      document.documentElement.classList.remove("preload");
      requestAnimationFrame(() => {
        els.forEach(el => {
          const r = el.getBoundingClientRect();
          if (r.top < window.innerHeight * 0.9 && r.bottom > 0) {
            el.classList.add("is-in");
          }
        });
      });
    });
  })();

  (function(){
    window.addEventListener("load", () => {
      const el = document.querySelector(".prompt");
      if (!el) return;

      const text = el.textContent.trim();
      if (!text) return;

      el.textContent = "";
      let i = 0;

      function type(){
        el.textContent = text.slice(0, i);
        i++;
        if (i <= text.length) setTimeout(type, 70);
      }

      setTimeout(type, 600);
    });
  })();

  (function(){
    const btn = document.getElementById("viewProjectsBtn");
    const panel = document.getElementById("projectsPanel");
    if (!btn || !panel) return;

    function open(){
      panel.classList.add("is-open");
      panel.setAttribute("aria-hidden", "false");

      panel.querySelectorAll(".revealOnOpen").forEach(el => {
        el.style.transition = "none";
        el.offsetHeight;
        el.style.transition = "";
      });

      panel.scrollIntoView({ behavior: "smooth", block: "start" });
    }

    btn.addEventListener("click", e => {
      e.preventDefault();
      open();
    });
  })();

  document.addEventListener("DOMContentLoaded", () => {
    const siteEl = document.getElementById("siteText");
    if (siteEl) {
      const text = siteEl.dataset.text || "";
      siteEl.textContent = "";
      let i = 0;

      (function type(){
        if (i <= text.length) {
          siteEl.textContent = text.slice(0, i);
          i++;
          setTimeout(type, 70);
        }
      })();
    }

    const title = document.getElementById("introTitle");
    if (title) {
      let lines = [];
      try { lines = JSON.parse(title.dataset.lines || "[]"); } catch {}

      title.innerHTML = "";

      const muted = document.createElement("span");
      muted.className = "muted";

      const accent = document.createElement("span");
      accent.className = "accent";

      const br = document.createElement("br");
      const cursor = document.createElement("span");
      cursor.className = "cursor";
      cursor.textContent = "|";

      title.appendChild(muted);
      title.appendChild(br);
      title.appendChild(accent);
      title.appendChild(cursor);

      const l1 = lines[0] || "";
      const l2 = lines[1] || "";

      let a = 0, b = 0;

      function t1(){
        if (a <= l1.length) {
          muted.textContent = l1.slice(0, a++);
          setTimeout(t1, 55);
        } else {
          setTimeout(t2, 220);
        }
      }

      function t2(){
        if (b <= l2.length) {
          accent.textContent = l2.slice(0, b++);
          setTimeout(t2, 55);
        } else {
          title.classList.add("floaty");
        }
      }

      t1();
    }
  });

  (function(){
    const tabs = document.querySelectorAll(".showcase__tab");
    const panels = document.querySelectorAll(".showcase__panel");
    if (!tabs.length || !panels.length) return;

    tabs.forEach(tab => {
      tab.addEventListener("click", () => {
        const target = tab.dataset.tab;

        tabs.forEach(t => t.classList.remove("is-active"));
        panels.forEach(p => p.classList.remove("is-active"));

        tab.classList.add("is-active");
        document
          .querySelector(`.showcase__panel[data-panel="${target}"]`)
          ?.classList.add("is-active");
      });
    });
  })();

  (function(){
    const lb = document.getElementById("lightbox");
    const img = document.getElementById("lightboxImg");
    if (!lb || !img) return;

    document.querySelectorAll(".cert-card").forEach(card => {
      card.addEventListener("click", () => {
        const full = card.dataset.full;
        if (!full) return;

        img.src = full;
        img.alt = card.querySelector("img")?.alt || "Certificado";
        lb.classList.add("is-open");
        lb.setAttribute("aria-hidden", "false");
        document.body.style.overflow = "hidden";
      });
    });

    function close(){
      lb.classList.remove("is-open");
      lb.setAttribute("aria-hidden", "true");
      document.body.style.overflow = "";
      img.src = "";
    }

    lb.addEventListener("click", e => {
      if (
        e.target.matches('[data-close="true"]') ||
        e.target.classList.contains("lightbox__backdrop")
      ) close();
    });

    document.addEventListener("keydown", e => {
      if (e.key === "Escape") close();
    });
  })();

})();
window.googleTranslateElementInit = function () {
  if (!document.getElementById("google_translate_element")) return;

  new google.translate.TranslateElement(
    {
      pageLanguage: "pt",
      includedLanguages: "pt,en,es",
      layout: google.translate.TranslateElement.InlineLayout.SIMPLE
    },
    "google_translate_element"
  );
};
